public class StringExercise {
    public static void main(String[] args) {

        String pinkFloyd = "We don't need no education\n" + "We don't need no thought control";
        System.out.println(pinkFloyd);

        String checkOut = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkOut);

        String boring = "In windows, the main drive is usually C:\\";
        System.out.println(boring);

        String backSlash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backSlash);


    }
}

